def add(a:int, b:int):
    """My add function
    Args:
        a (int): first number
        b (int): second number

    Returns:
        int: the sum of two number
    """
    return a + b 


def minus(a:int, b:int):
    """My minus function
    Args:
        a (int): first number
        b (int): second number

    Returns:
        int: the abstraction of two number
    """
    return a - b 